package results

import "github.com/xkilldash9x/scalpel-cli/pkg/schemas"

// ScoreConfig defines the parameters for the prioritization process,
// allowing for flexible scoring based on severity.
type ScoreConfig struct {
	SeverityWeights map[string]float64
}

// NormalizedFinding represents a finding that has been standardized for
// consistent processing by the prioritization and reporting stages.
type NormalizedFinding struct {
	schemas.Finding
	Score              float64
	NormalizedSeverity string
}

// Report aggregates the final, processed findings and provides a high-level summary.
// This is the final artifact generated by the results pipeline.
type Report struct {
	Findings []NormalizedFinding `json:"findings"`
	Summary  string              `json:"summary"`
}
